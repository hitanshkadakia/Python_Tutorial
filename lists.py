# Create a List
List=["python","java","C++"]
print(List)

# access the lists items
List=["python","java","C++"]
print(List[1])

# Negative indexing
List=["python","java","C++"]
print(List[-1])

# Range of indexes
List=["python","java","C++","kotlin"]
print(List[1:3])

# Change Item Value
List=["python","java","C++"]
List[1]="Flutter"
print(List)

# List Length
List=["python","java","C++"]
print(len(List))

# Remove Item
List=["python","java","C++"]
List.remove("java")
print(List)

# Delete item
List=["python","java","C++"]
del List[0]
print(List)