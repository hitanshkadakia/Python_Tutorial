print('hello')
# Output -> hello

print("hello")
# Output -> hello

print("""hello""")
# Output -> hello

# Strings are array
a="Hello, World!"
print(a[1])
# Output -> e

# Slicing
b="Hello, World!"
print(b[2:5])
# Output -> llo

# Negative indexing
c="Hello, World!"
print(c[-5:-2])
# Output -> orl

# strip
d="Hello, World!"
print(d.strip())
# Output -> Hello, World!

# lower case
e="Hello, World!"
print(e.lower())
# Output -> hello, world!

# upper case
f="Hello, World!"
print(f.upper())
# Output -> HELLO, WORLD!